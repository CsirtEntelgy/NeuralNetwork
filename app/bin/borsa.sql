CREATE VIEW mappa as 
  select b.azienda azienda,
        b.dettaglio dettaglio,
        b.sigla sigla,
        s.ultimoAggiornamento ultimoAggiornamento,
        s.abilitata abilitata,
        (select count(*) from dati where id_azienda=b.id_azienda) dati,
        b.id_azienda id_azienda
  from borsa b, stato s 
  where s.id_azienda=b.id_azienda;

CREATE VIEW abilitate as 
  select azienda,
        dettaglio,
        sigla,
        ultimoAggiornamento,
        dati,
        id_azienda
  from mappa 
  where abilitata=true;

create view StatoAzioni as 
      select b.id_azienda id_azienda,
             b.azienda azienda,
             b.dettaglio dettaglio,
             b.sigla sigla,
             s.ultimoAggiornamento ultimoAggiornamento,
             s.abilitata abilitata,
             b.dati dati
      from mappa b, stato s 
      where s.id_azienda=b.id_azienda;
create view azione as
  select b.id_azienda id_azienda,
         azienda ,
         dettaglio ,
         sigla ,
         data,
         apertura,
         massimo,
         minimo,
         ultimoValore,
         variazionePercentuale,
         volume 
    from borsa b join dati d on d.id_azienda=b.id_azienda;

CREATE TRIGGER trg_aggiornamento
INSTEAD OF UPDATE OF ultimoAggiornamento,abilitata ON StatoAzioni 
BEGIN
  UPDATE stato SET ultimoAggiornamento=NEW.ultimoAggiornamento,abilitata=NEW.abilitata
  WHERE id_azienda in (select id_azienda from borsa where sigla=OLD.sigla);
END;
